
public class Coche {
	String marca;
	String modelo;
	String fecha;
	String color;
	String kmUso;
	String precio;
	String estado;
	public Coche(String marca, String modelo, String fecha, String color, String kmUso, String precio, String estado) {
		super();
		this.marca = marca;
		this.modelo = modelo;
		this.fecha = fecha;
		this.color = color;
		this.kmUso = kmUso;
		this.precio = precio;
		this.estado = estado;
	}
}
